import { serve } from "https://deno.land/std@0.168.0/http/server.ts"

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    // Note: In production, this would clear the secrets from the environment
    // For now, we just return success since secrets are managed externally
    
    return new Response(
      JSON.stringify({
        success: true,
        message: 'Para limpar as credenciais completamente, remova os secrets GOOGLE_CLIENT_ID e GOOGLE_CLIENT_SECRET do painel administrativo do Supabase'
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      },
    )
  } catch (error) {
    return new Response(
      JSON.stringify({ error: error.message }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 400,
      },
    )
  }
})